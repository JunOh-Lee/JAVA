class Main {

  public static void main(String[] args) {
    //문자열을 변수에 담을 때는 아래와 같이 변수의 이름 앞에 String을 붙여준다.
    String first = "coding";
    System.out.println(first+""+"everybody");

    //아래와 같이 여러개의 변수(a,b)를 동시에 선언 할 수도 있다.
    String a, b;
    a = "coding";
    b = " everybody";
    System.out.println(a+b);
  }
}
